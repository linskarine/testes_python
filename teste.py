# Primeiro programa em Python - Bem-vindo ao mundo da programação!

# 1. Imprimindo mensagens
print("Hello, World!")  # Esta é a função print básica
print("Olá, Mundo!")    # Podemos escrever em português também!

# 2. Variáveis e tipos básicos
nome = "Estudante"      # String (texto)
idade = 18             # Integer (número inteiro)
altura = 1.75          # Float (número decimal)

# 3. Concatenação e formatação de strings
print("Meu nome é " + nome)
print(f"Eu tenho {idade} anos e {altura}m de altura")

# 4. Operações matemáticas básicas
soma = 5 + 3
subtracao = 10 - 4
multiplicacao = 6 * 2
divisao = 15 / 3

print(f"\nOperações matemáticas:")
print(f"5 + 3 = {soma}")
print(f"10 - 4 = {subtracao}")
print(f"6 * 2 = {multiplicacao}")
print(f"15 / 3 = {divisao}")

# 5. Input do usuário
resposta = input("\nQual é o seu nome? ")
print(f"Prazer em conhecer você, {resposta}!")